      <h2>ABOUT US</h2>

      <p class="about-desc">
        E Cell UVCE aims to enhance and nurture entrepreneurial skills among the
        students. It strives to be a well recognised centre of excellence for
        entrepreneurial development to create successful entrepreneurs. The main
        intention is to motivate and encourage students who have an inclination
        towards Entrepreneurship and uplift them to follow their dreams. The
        Cell, plans to provide Mentorship, Industry-Academia Interaction for
        further support, etc., keeping in mind the goal of producing successful
        startups and ventures from our premier institution into the world.
      </p>
      <a name="events1"></a>
      <h2 id="events">EVENTS</h2>

      <div class="events">
        <div class="bull-market">
          <h3>BULL MARKET</h3>
          <p>
            UVCE bull market is an online virtual stock investment, skill
            enhancement, online contest. It is a week-long contest to discover
            the next best stock investor among engineering students. It is an
            opportunity for all the young and competitive minds to learn the
            basics of trading and test their skills in stock market, but risk free.
          </p>
        </div>
        <div class="eureka">
          <h3>EUREKA</h3>
          <p>
            EUREKA is an event that helps foster upcoming Entrepreneurs and
            their innovative ideas. It aims to explore and develop skills by
            allowing participants to showcase their thinking and
            marketing expertise. The event sails through exceptional ideas, to 
            drafting a well-thought business model and pitching to a panel of investors.
          </p>
